package StringExample;

public class StringConcatination {

	
	public static void main(String[] args) {
		
		String s = "abc";
	    s.concat("pqr");
	    System.out.println(s);  // 

	    String s1 = "abc";
	    s1 = s1.concat("pqr");
	    System.out.println(s1);  // abcpqr
	    System.out.println(">>>>>>>>>>>>>>>");
	    
	    String s2 = "bhaskar";
	    s2 = s2.concat("reddy");
	    
	    String s3 = "bhaskar";
	    s3.concat("reddy");
	    System.out.println(s2);
	    System.out.println(s3);
	    
	    
	    
	    
		
	}
}
